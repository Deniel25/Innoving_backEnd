export class Variable {
    public id: number; 
    public valor: number; 
    public id_publicacion : number;
    public id_variable : number; 

    constructor(id: number, 
                valor: number, 
                id_publicacion : number,
                id_variable : number){
        this.id = id; 
        this.valor = valor; 
        this.id_publicacion = id_publicacion;
        this.id_variable = id_variable;

    }

}





